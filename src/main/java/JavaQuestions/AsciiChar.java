package JavaQuestions;

public class AsciiChar {

	public static void main(String[] args) {

		char x='D'; //97
		char y='d'; //65
		
		int AsciiValue_X=x;
		int AsciiValue_Y=y;
		
		System.out.println("Ascii value of x is: "+AsciiValue_X);
		System.out.println("Ascii value of y is: "+AsciiValue_Y);

	}

}
